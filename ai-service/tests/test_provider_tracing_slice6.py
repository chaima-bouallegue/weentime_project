# ---------- Fallback / deterministic_fallback kind --------------------------


def test_deterministic_fallback_kind_does_not_overwrite_provider_used() -> None:
    """The existing FallbackMetadata.provider_used literal is preserved (it's
    constrained to 'none' by the schema). We layer new keys without touching
    it."""
    response = AgentResponse(
        type="error",
        text="Je ne peux pas confirmer cette information.",
        intent="fallback.guard_rejected",
        confidence=1.0,
        actionResult={
            "kind": "deterministic_fallback",
            "fallback_used": True,
            "fallback_reason": "guard_rejected",
            "provider_used": "none",
        },
    )

    out = annotate_provider_metadata(response, provider_router=_router(), intent_before_llm="leave.balance")

    assert out.actionResult["llm_used"] is False
    assert out.actionResult["provider"] == "ollama"
    assert out.actionResult["model"] == "qwen2.5:3b"
    # Existing field untouched
    assert out.actionResult["provider_used"] == "none"
    assert out.actionResult["fallback_reason"] == "guard_rejected"


# ---------- Idempotency / re-annotation -------------------------------------


def test_annotation_is_idempotent() -> None:
    response = AgentResponse(
        type="answer",
        text="ok",
        intent="leave.balance",
        confidence=0.9,
        actionResult={"kind": "read_result"},
    )

    once = annotate_provider_metadata(response, provider_router=_router(), intent_before_llm="leave.balance")
    twice = annotate_provider_metadata(once, provider_router=_router(), intent_before_llm="leave.balance")

    assert twice.actionResult["llm_used"] is False
    assert twice.actionResult["provider"] == "ollama"


# ---------- Don't regress core response fields ------------------------------


def test_text_and_intent_are_not_modified() -> None:
    response = AgentResponse(
        type="confirm_action",
        text="Confirmez-vous cette demande ?",
        intent="leave.create",
        confidence=0.9,
        requiresConfirmation=True,
        confirmationId="cf-1",
        toolCalls=[ToolCallRecord(name="leave.create_request", arguments={}, status="pending_confirmation")],
        actionResult={"kind": "confirmation_summary", "summary": {"type": "annuel"}},
    )

    out = annotate_provider_metadata(response, provider_router=_router(), intent_before_llm="leave.create")

    assert out.text == "Confirmez-vous cette demande ?"
    assert out.intent == "leave.create"
    assert out.type == "confirm_action"
    assert out.requiresConfirmation is True
    assert out.confirmationId == "cf-1"
    assert out.toolCalls[0].name == "leave.create_request"
    assert out.actionResult["kind"] == "confirmation_summary"
    assert out.actionResult["summary"] == {"type": "annuel"}


