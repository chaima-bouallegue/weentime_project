# ---------- Provider-used path (rare — provider_response kind) --------------


def test_provider_response_kind_gets_llm_used_true() -> None:
    response = AgentResponse(
        type="answer",
        text="Generated by LLM.",
        intent="provider.response",
        confidence=0.0,
        actionResult={
            "kind": "provider_response",
            "providerName": "ollama",
            "model": "qwen2.5:3b",
            "latencyMs": 123.4,
        },
    )

    out = annotate_provider_metadata(response, provider_router=_router(), intent_before_llm="fallback.unknown")

    assert out.actionResult["llm_used"] is True
    assert out.actionResult["provider"] == "ollama"
    # When the provider response carries its own model, prefer that.
    assert out.actionResult["model"] == "qwen2.5:3b"
    assert out.actionResult["intent_before_llm"] == "fallback.unknown"
    assert out.actionResult["intent_after_llm"] == "provider.response"


