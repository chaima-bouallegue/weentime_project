"""Slice 6 — Ollama/Qwen provider tracing.

Tests for a uniform per-turn metadata annotation that lets observability /
frontend answer "did this turn use the LLM? which provider/model?".

Pure metadata. Does not change what the LLM does or when it is called.
"""
from __future__ import annotations

import pytest

from app.context.current_user import CurrentUserContext
from app.models.agent_models import AgentResponse, ToolCallRecord
from app.observability.provider_metadata import annotate_provider_metadata
from app.providers.router import ProviderRouter


def _router() -> ProviderRouter:
    return ProviderRouter(
        mode="ollama",
        providers={"ollama": _StubProvider()},
        default_model="qwen2.5:3b",
        fallback_model="phi3",
    )


def _disabled_router() -> ProviderRouter:
    return ProviderRouter(mode="disabled", default_model=None)


class _StubProvider:
    def provider_name(self) -> str:
        return "ollama"

    def supports_streaming(self) -> bool:
        return False

    def supports_tools(self) -> bool:
        return False


# ---------- Deterministic agent path (most turns) ---------------------------


def test_deterministic_response_gets_llm_used_false() -> None:
    response = AgentResponse(
        type="answer",
        text="Voici votre solde de conges.",
        intent="leave.balance",
        confidence=0.9,
        actionResult={"kind": "read_result", "data": {"total": 12}},
    )

    out = annotate_provider_metadata(response, provider_router=_router(), intent_before_llm="leave.balance")

    assert isinstance(out.actionResult, dict)
    assert out.actionResult["llm_used"] is False
    assert out.actionResult["provider"] == "ollama"
    assert out.actionResult["model"] == "qwen2.5:3b"
    assert out.actionResult["intent_before_llm"] == "leave.balance"
    assert "intent_after_llm" not in out.actionResult or out.actionResult["intent_after_llm"] is None
    # Existing fields preserved
    assert out.actionResult["kind"] == "read_result"
    assert out.actionResult["data"] == {"total": 12}


